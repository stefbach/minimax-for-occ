#!/usr/bin/env node
/**
 * sync-dialer-utils.js — copy the canonical phone-utils source from
 * web/lib/phone-utils.ts into dialer/src/_phone-utils.generated.ts so the
 * out-of-process dialer worker shares the exact same geo-routing tables.
 *
 * Run manually with `npm run sync-utils` (from web/) or via the
 * `sync-utils:check` script in CI / a pre-commit hook to detect drift.
 *
 * Usage:
 *   node web/scripts/sync-dialer-utils.js          # write
 *   node web/scripts/sync-dialer-utils.js --check  # exit 1 if out of sync
 */

const fs = require("node:fs");
const path = require("node:path");
const crypto = require("node:crypto");

const REPO_ROOT = path.resolve(__dirname, "..", "..");
const SRC = path.join(REPO_ROOT, "web", "lib", "phone-utils.ts");
const DEST = path.join(
  REPO_ROOT,
  "dialer",
  "src",
  "_phone-utils.generated.ts",
);

const BANNER = [
  "// ---------------------------------------------------------------------------",
  "// DO NOT EDIT — generated by web/scripts/sync-dialer-utils.js",
  "//",
  "// This file mirrors web/lib/phone-utils.ts so the dialer worker (out-of-",
  "// process from the Next.js app) can share the same geo-routing tables.",
  "// To update: edit web/lib/phone-utils.ts, then run `npm run sync-utils`",
  "// from the web/ directory.",
  "// ---------------------------------------------------------------------------",
  "",
].join("\n");

function build() {
  const src = fs.readFileSync(SRC, "utf8");
  const hash = crypto.createHash("sha256").update(src).digest("hex").slice(0, 16);
  return `${BANNER}// source-sha256: ${hash}\n\n${src}`;
}

function main() {
  const check = process.argv.includes("--check");
  const want = build();
  if (check) {
    const have = fs.existsSync(DEST) ? fs.readFileSync(DEST, "utf8") : "";
    if (have !== want) {
      console.error(
        "[sync-dialer-utils] dialer/src/_phone-utils.generated.ts is out of sync with web/lib/phone-utils.ts.\n" +
          "Run `npm run sync-utils` from web/ and commit the result.",
      );
      process.exit(1);
    }
    console.log("[sync-dialer-utils] in sync");
    return;
  }
  fs.mkdirSync(path.dirname(DEST), { recursive: true });
  fs.writeFileSync(DEST, want, "utf8");
  console.log(`[sync-dialer-utils] wrote ${path.relative(REPO_ROOT, DEST)}`);
}

main();
